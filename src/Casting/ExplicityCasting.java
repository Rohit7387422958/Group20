package Casting;

public class ExplicityCasting 
{

	public static void main(String[] args) 
	{

		//converting higher data type into lower data type info is called explicit casting
		//explicit casting is narrowing  casting 
		//where memory size goes on decreasing ,there is data loss take place.
		
		double a=99.99;
		System.out.println(a);
		
		int b=(int)a;
		System.out.println(b);
		

	}

}
