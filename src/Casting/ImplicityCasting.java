package Casting;

public class ImplicityCasting 
{

	public static void main(String[] args) 
	{
		
		//converting lower data type into higher data type info is called implicit casting
		//Implicit casting is winding casting 
		//where memory size goes on increasing ,there is no data loss.
		{
			      int a=10;
			      System.out.println(a);
			      
			      double b=a;
			      System.out.println(b);
	             

	    }
	}
}

