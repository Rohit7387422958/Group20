package loops;

public class FoorLoop5 
{

	public static void main(String[] args)
	{
		//i want the print the number 0 to 10 ant start at 1 increment by 10
				for(int a=1;a<=10;a++)
				{
					System.out.println(a*6);
				}
				System.out.println("=================");
				
				
				for(int a=10;a>=1;a--)
				{
					System.out.println(a*6);
					
				}
	}

}
