package loops;

public class ForLoop 
{

	public static void main(String[] args) 
	{
		// i want to print number 0-10 and increment by 1
				for(int a=0;a<=10;a++)
				{
					System.out.println(a);
				}
				System.out.println("============");
				for(int a=10;a>=0;a--)
				{
					System.out.println(a);
				}

	}

}
