package loops;

public class ForLoop3
{

	public static void main(String[] args) 
	{

		//i want to print 5 table in rev order
		
		for(int a=50;a>=5;a=a-5)
		{
			System.out.println(a);
		}
		System.out.println("Hi");
		
		
		System.out.println("=============");
		for(int a=5;a<=50;a=a+5)
		{
			System.out.println(a);
		}
	}

}
