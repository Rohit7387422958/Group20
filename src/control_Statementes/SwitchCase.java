package control_Statementes;

public class SwitchCase {

	public static void main(String[] args) {
		 
		//1--->Welcome to 1st Engg
				//2-->Welcome to 2nd Engg
				//3--->Welcome to 3rd Engg
				//4--->Welcome to 4th Engg
		      int year=4;
		      switch (year) {
			case 1:System.out.println("Welcome to 1st Engg");
				
				break;
			case 2:System.out.println("Welcome to 2nd Engg");
			
			break;
			case 3:System.out.println("Welcome to 3rd Engg");
			
			break;
			case 4:System.out.println("Welcome to 4th Engg");
			
			break;
			
			default:System.out.println("please enter the years between 1 to 4");
				break;
			}
			}

	}


